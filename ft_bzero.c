#include "libft.h"

void	ft_bzero(void *ptr, size_t num)
{
	char	*p;
	size_t	i;

	if (!num)
		return ;
	i = 0;
	p = (char *)ptr;
	while (i < num)
	{
		p[i] = 0;
		i++;
	}
}
